package com.zkf.datascreen.model;

public class MyBean {
    private String S;
    private Float F;

    public MyBean(String s, Float f) {
        S = s;
        F = f;
    }

    public String getS() {
        return S;
    }

    public void setS(String s) {
        S = s;
    }

    public Float getF() {
        return F;
    }

    public void setF(Float f) {
        F = f;
    }
}
